CREATE TABLE SALES(
    SALES_CODE          VARCHAR2(12),
    NUM                 NUMBER(6),
    ID                  VARCHAR2(20),
    PRODUCT_CODE        VARCHAR2(30),
    AMOUNT              NUMBER(4),
    SALES_DATE          TIMESTAMP,
    DELIVER_STATUS      VARCHAR2(100),
    CONSTRAINT SALES_CODE_PK PRIMARY KEY(SALES_CODE),
    CONSTRAINT SALES_ID_FK   FOREIGN KEY(ID) REFERENCES MEMBER(ID),
    CONSTRAINT SALES_PRODUCT_CODE_FK FOREIGN KEY(PRODUCT_CODE) REFERENCES PRODUCT(PRODUCT_CODE)
    );